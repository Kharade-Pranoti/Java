// A program to find the maximum number 

class Max_Num {
	public static void main (String[] args) {
	   int num1 , num2 , max ;
	   
	   num1 = 563;
	   num2 = 536;
	  
	  // condition to check max number 
	   if(num1 > num2)
	   {
	       max = num1;
	   }
	   else
	   {
	       max = num2;
	   }

     // printing the maximum number
	   System.out.println("The maximum number among two numbers is "+max);
	   
	}   
}
